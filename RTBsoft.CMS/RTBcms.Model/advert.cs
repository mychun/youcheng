namespace RTBcms.Model
{
    using System;

    [Serializable]
    public class advert
    {
        private DateTime _add_time = DateTime.Now;
        private int _id;
        private decimal _price = 0M;
        private string _remark;
        private string _target;
        private string _title;
        private int _type;
        private int _view_height = 0;
        private int _view_num = 0;
        private int _view_width = 0;

        public DateTime add_time
        {
            get
            {
                return this._add_time;
            }
            set
            {
                this._add_time = value;
            }
        }

        public int id
        {
            get
            {
                return this._id;
            }
            set
            {
                this._id = value;
            }
        }

        public decimal price
        {
            get
            {
                return this._price;
            }
            set
            {
                this._price = value;
            }
        }

        public string remark
        {
            get
            {
                return this._remark;
            }
            set
            {
                this._remark = value;
            }
        }

        public string target
        {
            get
            {
                return this._target;
            }
            set
            {
                this._target = value;
            }
        }

        public string title
        {
            get
            {
                return this._title;
            }
            set
            {
                this._title = value;
            }
        }

        public int type
        {
            get
            {
                return this._type;
            }
            set
            {
                this._type = value;
            }
        }

        public int view_height
        {
            get
            {
                return this._view_height;
            }
            set
            {
                this._view_height = value;
            }
        }

        public int view_num
        {
            get
            {
                return this._view_num;
            }
            set
            {
                this._view_num = value;
            }
        }

        public int view_width
        {
            get
            {
                return this._view_width;
            }
            set
            {
                this._view_width = value;
            }
        }
    }
}

